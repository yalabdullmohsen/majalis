-- جدول الكتب الإسلامية
CREATE TABLE IF NOT EXISTS books (
  id              uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  title           text NOT NULL,
  author          text NOT NULL,
  -- هجري أو ميلادي (سنة الوفاة للمؤلف، أو سنة الطباعة)
  year_hijri      integer,
  year_ce         integer,
  -- التصنيف الرئيسي: عقيدة، فقه، حديث، تفسير، سيرة، أصول، فقه اللغة، عام
  category        text NOT NULL DEFAULT 'عام',
  -- المستوى: مبتدئ، متوسط، متقدم
  level           text NOT NULL DEFAULT 'متوسط',
  -- وصف أكاديمي (لا آيات، لا أحاديث — محتوى توثيقي فقط)
  description     text,
  -- رابط خارجي للكتاب أو الناشر
  external_url    text,
  -- عدد الصفحات التقريبي
  pages_approx    integer,
  -- الناشر / دار النشر
  publisher       text,
  -- لغة الكتاب
  language        text NOT NULL DEFAULT 'ar',
  -- الطبعة الموصى بها
  edition         text,
  -- حالة المراجعة — false دائماً للمحتوى المُولَّد
  is_approved     boolean NOT NULL DEFAULT false,
  verified_by     uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  created_at      timestamptz NOT NULL DEFAULT now(),
  updated_at      timestamptz NOT NULL DEFAULT now()
);

-- فهارس الكتب
CREATE INDEX IF NOT EXISTS idx_books_category ON books(category);
CREATE INDEX IF NOT EXISTS idx_books_level    ON books(level);
CREATE INDEX IF NOT EXISTS idx_books_author   ON books(author);
CREATE INDEX IF NOT EXISTS idx_books_approved ON books(is_approved);

-- جدول ربط الكتب بمسارات التعلم
CREATE TABLE IF NOT EXISTS learning_path_books (
  id              uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  -- مسار التعلم (slug أو اسم المسار)
  path_slug       text NOT NULL,
  book_id         uuid NOT NULL REFERENCES books(id) ON DELETE CASCADE,
  -- ترتيب الكتاب في المسار (1 = أول كتاب)
  position        integer NOT NULL DEFAULT 1,
  -- ملاحظة على الكتاب في هذا المسار تحديداً
  path_note       text,
  created_at      timestamptz NOT NULL DEFAULT now(),
  UNIQUE(path_slug, book_id)
);

CREATE INDEX IF NOT EXISTS idx_lpb_path ON learning_path_books(path_slug);
CREATE INDEX IF NOT EXISTS idx_lpb_book ON learning_path_books(book_id);

-- تحديث updated_at تلقائياً
CREATE OR REPLACE FUNCTION update_books_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS books_updated_at ON books;
CREATE TRIGGER books_updated_at
  BEFORE UPDATE ON books
  FOR EACH ROW EXECUTE FUNCTION update_books_updated_at();

