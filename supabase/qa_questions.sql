-- ---------------------------------------------------------------------
-- 1) جدول الأسئلة والأجوبة
-- ---------------------------------------------------------------------
create table if not exists qa_questions (
  id           uuid primary key default gen_random_uuid(),
  question     text not null,                              -- السؤال
  answer       text not null,                              -- الجواب
  category     text not null,                              -- التصنيف
  ruling_type  text,                                       -- نوع الحكم (لأحكام الشرعية فقط)
  evidence     text,                                       -- الدليل الشرعي
  reference    text,                                       -- المرجع
  reliability  text not null default 'يحتاج مراجعة'        -- درجة الاعتماد
                 check (reliability in ('معتمد', 'يحتاج مراجعة')),
  is_published boolean not null default false,             -- حالة النشر (منشور / مسودة)
  created_at   timestamptz not null default now(),         -- تاريخ الإضافة

  -- التصنيفات المسموحة
  constraint qa_category_valid check (
    category in (
      'أحكام شرعية', 'قصص الأنبياء', 'سير الصالحين',
      'السيرة النبوية', 'الصحابة', 'ألغاز فقهية'
    )
  ),
  -- أنواع الحكم المسموحة (أو فارغ)
  constraint qa_ruling_valid check (
    ruling_type is null or ruling_type in (
      'حلال', 'حرام', 'مكروه', 'مباح', 'سنة', 'مندوب'
    )
  )
);

create index if not exists qa_questions_category_idx on qa_questions (category);
create index if not exists qa_questions_published_idx on qa_questions (is_published);
create index if not exists qa_questions_created_idx   on qa_questions (created_at desc);

-- ---------------------------------------------------------------------
-- 2) أمان مستوى الصف (RLS)
--    الجميع يقرأ المنشور فقط، والمشرف يدير كل شيء.
-- ---------------------------------------------------------------------
alter table qa_questions enable row level security;

-- تعتمد هذه السياسة على دالة is_admin() المعرّفة في 01_schema.sql.
-- إن لم تكن موجودة، شغّل المخطط الأساسي أولًا.

drop policy if exists "قراءة الأسئلة المنشورة" on qa_questions;
create policy "قراءة الأسئلة المنشورة"
  on qa_questions for select
  using (is_published = true or is_admin());

drop policy if exists "المشرف يدير الأسئلة" on qa_questions;
create policy "المشرف يدير الأسئلة"
  on qa_questions for all
  using (is_admin()) with check (is_admin());

