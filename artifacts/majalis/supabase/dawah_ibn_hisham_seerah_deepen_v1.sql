-- تصحيح وتوسيع دورة "دعوة القبائل والهجرة" (مسار dawah) من السيرة النبوية
-- لابن هشام. اكتشاف أثناء الفحص (لا عيّنة عشوائية، فحص مباشر بطلب
-- المنسِّق لتنسيق هذه الدورة تحديدًا مع dawah-raheeq): وصف العنصر الأول
-- نفسه يذكر صراحةً "بيعتا العقبة الأولى والثانية"، لكن الدورة كانت
-- تحوي عنصرًا واحدًا فقط لبيعة العقبة الأولى — **بيعة العقبة الثانية
-- غائبة تمامًا** رغم كونها الحدث المحوري (بيعة الحرب) المؤدي مباشرة
-- للهجرة. كذلك كان الترتيب الزمني (sort_order) غير صحيح: عنصر "الهجرة"
-- (sort_order=2) يسبق عنصر "بيعة العقبة الأولى" (sort_order=3)، رغم أن
-- الهجرة تأتي زمنيًا بعد كلتا البيعتين لا قبلهما. تحقَّقت تفاصيل بيعة
-- العقبة الثانية عبر ويكيبيديا العربية (73 رجلاً وامرأتان، السنة
-- الثالثة عشرة من البعثة، بيعة القتال/النصرة بخلاف الأولى، مقدمة
-- مباشرة للهجرة). وصف الدورة حُدِّث ليُفصح صراحةً عن التنسيق الزمني مع
-- dawah-raheeq (تتوقف عند الإسراء والمعراج) كما طلب المنسِّق.

BEGIN;

-- 1) تصحيح الترتيب الزمني للعنصرين الموجودين + توحيد الأوزان على 4
--    عناصر بدل 3 (100/4 = 25 بالضبط لكل عنصر)
UPDATE learning_items SET weight = 25.00
  WHERE id = 'c6a94620-f7d6-4131-9024-cad009ddb4c2'; -- عرض النبي على القبائل (sort_order يبقى 1)

UPDATE learning_items SET sort_order = 2, weight = 25.00
  WHERE id = '239b458f-1a02-4466-8a1b-5f1abb277e63'; -- بيعة العقبة الأولى (كان sort_order=3)

UPDATE learning_items SET sort_order = 4, weight = 25.00
  WHERE id = '58273998-1af0-471a-a7f4-090bbe9f4f14'; -- الهجرة (كان sort_order=2 قبل بيعتي العقبة زمنيًا خطأً)

-- 2) تحديث وصف الدورة: تسلسل زمني صحيح + إفصاح صريح عن التنسيق مع
--    dawah-raheeq
UPDATE courses SET description =
  'قراءة موجَّهة في السيرة النبوية لابن هشام: عرض النبي ﷺ نفسه على القبائل، بيعة العقبة الأولى، بيعة العقبة الثانية (بيعة الحرب)، والهجرة كتحوّل في منهج الدعوة — تكملةً زمنية لمقرر "مراحل الدعوة النبوية الأولى" (الرحيق المختوم) الذي يتوقف عند الإسراء والمعراج.'
  WHERE id = 'fb6dd555-f38b-46a4-b602-149d89a42cf9';

-- 3) إضافة العنصر الغائب: بيعة العقبة الثانية (بين الأولى والهجرة زمنيًا)
WITH new_item AS (
  INSERT INTO learning_items
    (unit_id, item_type, title, description, sort_order, status, is_approved,
     is_required, completion_method, completion_threshold, session_estimate, weight)
  VALUES
    ('d51f414c-5158-41a6-a95f-12fd7d6c98b5', 'book',
     'بيعة العقبة الثانية',
     'من "السيرة النبوية" لابن هشام: بايع النبي ﷺ في ذي الحجة من السنة الثالثة عشرة من البعثة (قبل الهجرة بنحو ثلاثة أشهر) 73 رجلاً وامرأتين من الأنصار (11 من الأوس و62 من الخزرج) — وسُمّيت "بيعة الحرب" لاشتمالها على شرط النصرة والقتال دفاعًا عن النبي ﷺ، بخلاف بيعة العقبة الأولى التي اقتصرت على أسس إسلامية عامة دون قتال. كانت هذه البيعة المقدمة المباشرة لإذن الهجرة إلى المدينة.',
     3, 'published', true, true, 'manual_confirm', 100.00, 1.0, 25.00)
  RETURNING id
