-- Phase 4 QA enrichment seed
-- Safe, additive, and source-linked. Does not delete or overwrite existing records.

CREATE TABLE IF NOT EXISTS qa_categories (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  name text NOT NULL,
  slug text UNIQUE NOT NULL,
  description text,
  created_at timestamptz DEFAULT now()
);

CREATE TABLE IF NOT EXISTS qa_questions (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  question text NOT NULL,
  answer text NOT NULL,
  category_id uuid REFERENCES qa_categories(id) ON DELETE SET NULL,
  ruling_type text,
  evidence text,
  reference text,
  status text DEFAULT 'published',
  review_status text DEFAULT 'approved',
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

