    description = 'من المصطلحات التأسيسية اللازمة لفهم طبقات الإسناد: الصحابي عند جمهور المحدثين من لقي النبي ﷺ مؤمناً به ومات على الإسلام، ولو لم تطل صحبته أو لم يروِ عنه شيئاً. والتابعي من لقي صحابياً واحداً على الأقل مؤمناً ومات على الإسلام، وسُمّوا بذلك لأنهم تبعوا الصحابة في الفضل والعلم. يستند تعظيم مكانة الصحابة في التصنيف الحديثي إلى نصوص قرآنية صريحة، منها قوله تعالى: ﴿وَالسَّابِقُونَ الْأَوَّلُونَ مِنَ الْمُهَاجِرِينَ وَالْأَنصَارِ وَالَّذِينَ اتَّبَعُوهُم بِإِحْسَانٍ رَّضِيَ اللَّهُ عَنْهُمْ وَرَضُوا عَنْهُ﴾ (التوبة: 100)، وفيها تنويه صريح بمن اتَّبع الصحابة بإحسان (أي التابعين). أهمية هذا التصنيف في علم المصطلح: كل الأحاديث المرفوعة والموقوفة تُنسَب لأحد هذين الجيلين في أول السند، وضبط حدود كل طبقة أساس لمعرفة اتصال الإسناد وانقطاعه في الأنواع الأخرى المدروسة في هذا المقرر (المرسل والمنقطع).',
    session_estimate = 1.0,
    weight = 20.00,
    updated_at = now()
  WHERE id = 'd2145f1c-36f2-4589-a25a-af31ee66b9c1';

  UPDATE learning_paths lp SET total_sessions = (
    SELECT sum(li.session_estimate)::int
    FROM path_stages ps JOIN courses c ON c.stage_id = ps.id
    JOIN course_units cu ON cu.course_id = c.id
    JOIN learning_items li ON li.unit_id = cu.id
    WHERE ps.path_id = lp.id
