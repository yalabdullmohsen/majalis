-- علم الجرح والتعديل — لم يُذكر إطلاقاً في أي كورس بمسار مصطلح الحديث
-- الثلاثة (mustalah-intro/mustalah-aqsam-hadith/nukhbat-fikr)، رغم أنه
-- الأداة العملية التي تُصنَّف بها الأحاديث لصحيح/حسن/ضعيف أصلاً. أُضيف
-- عنصر واحد جديد بصياغة تعليمية أصلية استناداً لبنية "معرفة الثقات
-- والضعفاء" (النوع الحادي والستون من مقدمة ابن الصلاح) المُتحقَّق منها
-- عبر WebFetch من ar.wikisource.org، ولتراتب ألفاظ الجرح والتعديل وهو
-- إطار تصنيفي مستقر عبر كتب المصطلح (لا مأخوذ حرفياً من مصدر واحد بعينه).
DO $$
DECLARE
  v_unit_id uuid := 'd75b1507-ba19-4383-a17d-00767904bd5a';
BEGIN
  INSERT INTO learning_items (unit_id, item_type, title, description, session_estimate, weight, sort_order, status, is_approved)
  VALUES
  (v_unit_id, 'lesson', 'الجرح والتعديل: أصوله ومراتبه',
   'الجرح والتعديل: علم يبحث في أحوال رواة الحديث توثيقاً (تعديلاً) أو تضعيفاً (جرحاً)، وهو الأساس العملي الذي تُبنى عليه أحكام الصحة والحسن والضعف المذكورة في هذا المقرر. وصفه ابن الصلاح بأنه "من أجلّ علوم الحديث وأفخمها"، وأكَّد أن ممارسته ليست غيبة محرَّمة بل نصيحة واجبة لصيانة الشريعة من دخول الكذب والخطأ فيها؛ ولذا اشترط العلماء فيمن يتصدى له العلم والورع والإنصاف، وحذَّروا من أخطاء وقع فيها بعض من جرحوا رواة بما لا يصح. تتدرج ألفاظ التعديل من الأعلى (كـ"ثقة ثقة" أو "أوثق الناس") إلى الأدنى (كـ"صدوق" أو "لا بأس به")، وألفاظ الجرح من الأخف (كـ"لَيِّن الحديث") إلى الأشد (كـ"متروك" ثم "كذاب" أو "وضّاع")؛ وتقرَّرت عند المحدثين قاعدة أن الجرح المُفسَّر (المُبيَّن سببه) مقدَّمٌ على التعديل عند التعارض، لأن الجارح عنده زيادة علم خفيت على المُعدِّل.',
   1.0, 20.00, 5, 'published', true);

  UPDATE learning_items SET weight = 20.00, updated_at = now()
  WHERE unit_id = v_unit_id AND sort_order IN (1,2,3,4);

  UPDATE learning_paths lp SET total_sessions = (
    SELECT sum(li.session_estimate)::int
    FROM path_stages ps JOIN courses c ON c.stage_id = ps.id
    JOIN course_units cu ON cu.course_id = c.id
    JOIN learning_items li ON li.unit_id = cu.id
    WHERE ps.path_id = lp.id
  ), updated_at = now() WHERE lp.slug = 'mustalah-hadith';
END $$;
