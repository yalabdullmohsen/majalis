--   1. باب تفسير التوحيد وشهادة أن لا إله إلا الله — حديث معاذ بن جبل
--      "يا معاذ، هل تدري ما حق الله على العباد وما حق العباد على الله؟...
--      فإن حق الله على العباد أن يعبدوه ولا يشركوا به شيئاً" — رواه مسلم
--      (كتاب الإيمان، حديث رقم 30 حسب ترقيم الطبعة المعتمدة في ويكي مصدر،
--      تحقَّق عبر WebFetch من نص الحديث الكامل بالسند: هداب بن خالد →
--      همام → قتادة → أنس بن مالك → معاذ بن جبل). وله أصل أيضاً في صحيح
--      البخاري بلفظ قريب.
--   2. باب من حقق التوحيد دخل الجنة بغير حساب — حديث السبعين ألفاً الذين
--      يدخلون الجنة بغير حساب ("هم الذين لا يسترقون ولا يتطيرون وعلى
--      ربهم يتوكلون") — متفق عليه، ورد بأسانيد متعددة عن ابن عباس وعمران
--      بن حصين وأبي هريرة رضي الله عنهم في صحيح مسلم (كتاب الإيمان)
--      وصحيح البخاري (كتاب الرقاق)، تحقَّق عبر WebFetch من وروده الفعلي
--      في صحيح مسلم بهذا اللفظ. لم يُذكر رقم حديث محدد لعدم التمكن من
--      تأكيده حرفياً بثقة كافية (بنفس انضباط الاكتفاء بـ"متفق عليه" حين
--      لا يتوفر رقم مؤكَّد، كما في دفعات سابقة).
--   3. باب الخوف من الشرك — الآية القرآنية (إبراهيم: 35) "وَإِذْ قَالَ
--      إِبْرَٰهِيمُ رَبِّ ٱجْعَلْ هَٰذَا ٱلْبَلَدَ ءَامِنًۭا وَٱجْنُبْنِى
--      وَبَنِىَّ أَن نَّعْبُدَ ٱلْأَصْنَامَ" — تحقَّقت حرفياً من الملف
--      المحلي public/data/quran/surah-014.json (numberInSurah: 35).
--      وحديث "إن أخوف ما أخاف عليكم الشرك الأصغر... الرياء" — رواه أحمد
--      عن محمود بن لبيد رضي الله عنه (مسند أحمد)، تحقَّق عبر WebFetch من
--      وروده بهذا السند واللفظ. **ذُكر صراحة في النص أن في سند هذا الحديث
--      كلاماً بين أهل العلم (فيه انقطاع/جهالة راوٍ عند بعضهم، وصحَّحه آخرون
--      بشواهده) — لم يُقدَّم كحديث صحيح قطعي بلا تحفظ**، بنفس انضباط عرض
--      الخلاف الحديثي الصريح المُتَّبع في المقالة الثالثة سابقاً.
--   4. باب من الشرك: الذبح لغير الله — حديث "لعن الله من ذبح لغير الله"
--      (ضمن حديث أطول فيه لعنات أخرى) — رواه مسلم عن علي بن أبي طالب رضي
--      الله عنه (كتاب الأضاحي)، تحقَّق عبر WebFetch من وروده في صحيح مسلم
--      بهذا السند. لم يُذكر رقم حديث محدد للسبب نفسه أعلاه.
--
