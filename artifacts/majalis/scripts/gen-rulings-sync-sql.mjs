#!/usr/bin/env node
/**
 * يولّد SQL لمزامنة public/data/rulings-encyclopedia/chunks/*.json (527
 * سجلاً، الناتج الحقيقي لـ generate-rulings-encyclopedia.mjs) مع جدول
 * sharia_rulings الحي — يطابق تحويل toDbRow() في lib/rulings-db-seed.mjs
 * حرفياً (بعد إصلاح خطأ verification_status: 'pending_review' لم يكن
 * ضمن قيم CHECK constraint الحية، اكتُشف بمحاولة إدراج فعلية 2026-07-18).
 *
 * ON CONFLICT (external_key) DO UPDATE — يطابق نية seedRulingsFromFilesystem()
 * الحقيقية (upsert بـignoreDuplicates:false = تحديث السجلات المطابقة، لا
 * تجاهلها) بدل الاكتفاء بإدراج الجديد فقط، حتى تنعكس إصلاحات تصنيف سابقة
 * (مثل "الحج"→"الحج والعمرة") على الصفوف الحية القديمة أيضاً.
 *
 * التشغيل: node scripts/gen-rulings-sync-sql.mjs > /tmp/rulings-sync.sql
 */
import { readFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const APP_ROOT = resolve(HERE, "..");
const DATA_DIR = resolve(APP_ROOT, "public/data/rulings-encyclopedia");

function esc(s) {
  if (s === undefined || s === null) return "NULL";
  return `'${String(s).replace(/'/g, "''")}'`;
}
function escJsonb(v) {
  return `${esc(JSON.stringify(v ?? []))}::jsonb`;
}
function escTextArray(arr) {
  if (!arr || arr.length === 0) return "'{}'::text[]";
  return `ARRAY[${arr.map((s) => esc(s)).join(",")}]::text[]`;
}

// مطابقة toDbRow() في lib/rulings-db-seed.mjs حرفياً (بعد الإصلاح)
function toDbRow(item) {
  return {
    external_key: item.external_key || item.slug || item.id,
    title: item.title,
    summary: item.summary ?? null,
    body: item.body || item.summary || item.title,
    category: item.category || "فقه عام",
    subcategory: item.subcategory ?? null,
    subcategories: item.subcategories ?? [],
    keywords: item.keywords ?? [],
    evidence: item.evidence ?? [],
    references: item.references ?? [],
    quran_evidence: item.quran_evidence ?? [],
    sunnah_evidence: item.sunnah_evidence ?? [],
    scholar_opinions: item.scholar_opinions ?? [],
    prevailing_view: item.prevailing_view ?? null,
    hadith_grade: item.hadith_grade ?? null,
    benefits: item.benefits ?? [],
    importance_score: item.importance_score ?? 50,
    popularity_score: item.popularity_score ?? 0,
    search_count: item.search_count ?? 0,
    verification_status: item.verification_status === "pending_review" ? "pending" : item.verification_status || "approved",
    status: "approved",
    published_at: item.published_at || item.created_at || new Date().toISOString(),
  };
}

// اكتُشف 2026-07-18: chunks/ يحوي ملفات يتيمة قديمة من تصنيفات أُعيدت
// تسميتها منذ جلسات سابقة (الحج.json/المواريث.json/النوازل.json —
// الأسماء القديمة قبل إصلاح التصنيف في commit 2dcddead) لا يحذفها
// المولِّد أبداً (لا تنظيف قبل الكتابة). القراءة عبر readdirSync ساذجة
// كانت لتُدرِج هذه اليتامى (19 حكماً تكرارياً) — الدالة الحقيقية
// loadSeedItemsFromFilesystem() في rulings-db-seed.mjs سليمة من هذا لأنها
// تتبع manifest.json (يُعاد توليده بأسماء التصنيف الحالية فقط)، فطابقتها هنا.
const manifest = JSON.parse(readFileSync(resolve(DATA_DIR, "manifest.json"), "utf8"));
const items = [];
for (const chunk of manifest.chunks || []) {
  const rows = JSON.parse(readFileSync(resolve(DATA_DIR, chunk.file), "utf8"));
  items.push(...rows);
}

console.log("-- Auto-generated by scripts/gen-rulings-sync-sql.mjs — DO NOT EDIT BY HAND");
console.log(`-- ${items.length} حكماً من public/data/rulings-encyclopedia/chunks/*.json`);
console.log("BEGIN;");

const BATCH = 50;
for (let i = 0; i < items.length; i += BATCH) {
  const batch = items.slice(i, i + BATCH).map(toDbRow);
  const values = batch
    .map(
      (r) =>
        `(${esc(r.external_key)}, ${esc(r.title)}, ${esc(r.summary)}, ${esc(r.body)}, ${esc(r.category)}, ${esc(r.subcategory)}, ${escTextArray(r.subcategories)}, ${escTextArray(r.keywords)}, ${escJsonb(r.evidence)}, ${escJsonb(r.references)}, ${escJsonb(r.quran_evidence)}, ${escJsonb(r.sunnah_evidence)}, ${escJsonb(r.scholar_opinions)}, ${esc(r.prevailing_view)}, ${esc(r.hadith_grade)}, ${escTextArray(r.benefits)}, ${r.importance_score}, ${r.popularity_score}, ${r.search_count}, ${esc(r.verification_status)}, ${esc(r.status)}, ${esc(r.published_at)})`,
    )
    .join(",\n  ");

  console.log(`INSERT INTO sharia_rulings (external_key, title, summary, body, category, subcategory, subcategories, keywords, evidence, "references", quran_evidence, sunnah_evidence, scholar_opinions, prevailing_view, hadith_grade, benefits, importance_score, popularity_score, search_count, verification_status, status, published_at)
VALUES
  ${values}
ON CONFLICT (external_key) DO UPDATE SET
  title = EXCLUDED.title,
  summary = EXCLUDED.summary,
  body = EXCLUDED.body,
  category = EXCLUDED.category,
  subcategory = EXCLUDED.subcategory,
  subcategories = EXCLUDED.subcategories,
  keywords = EXCLUDED.keywords,
  evidence = EXCLUDED.evidence,
  "references" = EXCLUDED."references",
  quran_evidence = EXCLUDED.quran_evidence,
  sunnah_evidence = EXCLUDED.sunnah_evidence,
  scholar_opinions = EXCLUDED.scholar_opinions,
  prevailing_view = EXCLUDED.prevailing_view,
  hadith_grade = EXCLUDED.hadith_grade,
  benefits = EXCLUDED.benefits,
  importance_score = EXCLUDED.importance_score,
  updated_at = now();`);
}

console.log("COMMIT;");
