import { DEMO_QUIZ_QUESTIONS } from "../src/lib/quiz-seed.ts";

// اكتُشف 2026-07-18: quiz_questions.level مقيَّد بـCHECK constraint يقبل
// فقط 'أساسي'|'متوسط'|'متقدم'|'صعب' (عربي) — لا 'beginner'/'intermediate'/
// 'advanced' كما كانت تفترض upsertQuizSeedToDb() القديمة في supabase.ts
// (خطأ حقيقي كان يُسقِط أي مزامنة فوراً). متوسط/صعب متطابقان أصلاً؛
// سهل←أساسي هو التعيين الوحيد المطلوب.
const LEVEL_MAP = { "سهل": "أساسي", "متوسط": "متوسط", "صعب": "صعب" };

function esc(s) {
  if (s === undefined || s === null) return "NULL";
  return `'${String(s).replace(/'/g, "''")}'`;
}

const rows = DEMO_QUIZ_QUESTIONS.filter((q) => q.status !== "draft");

console.log("-- Auto-generated by scripts/gen-quiz-sync-sql.mjs — DO NOT EDIT BY HAND");
console.log(`-- ${rows.length} سؤالاً من quiz-seed.ts`);
console.log("BEGIN;");

const BATCH = 100;
for (let i = 0; i < rows.length; i += BATCH) {
  const batch = rows.slice(i, i + BATCH);
  const values = batch
    .map((q) => {
      const level = LEVEL_MAP[q.level] || q.level;
      const category = q.category || q.section;
      return `(${esc(q.section)}, ${esc(category)}, ${esc(level)}, ${esc(q.question)}, ${esc(q.answer)}, true, false, ${esc(q.explanation)}, ${esc(q.reference)})`;
    })
    .join(",\n  ");

  console.log(`INSERT INTO quiz_questions (section, category, level, question, answer, is_published, is_used, explanation, source_reference)
VALUES
  ${values}
ON CONFLICT (question) DO NOTHING;`);
}

console.log("COMMIT;");
