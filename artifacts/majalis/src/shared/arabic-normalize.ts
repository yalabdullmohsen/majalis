/**
 * arabic-normalize.ts
 * وحدة التطبيع العربي المشتركة — للفهرسة والبحث فقط.
 *
 * ⚠️ قاعدة حرجة: هذه الوحدة للفهرسة والمقارنة الداخلية فقط.
 *    النص المعروض للمستخدم (آيات / أحاديث / أذكار) يُعرض دائماً
 *    بتشكيله الكامل بدون أي تعديل.
 */

/** خرائط الأرقام الهندية العربية (٠-٩) والفارسية/الأردية الممتدة (۰-۹) إلى
 *  أرقام لاتينية عادية (0-9) — ضرورية للبحث/الفرز/التصفية: لوحة مفاتيح
 *  العربية على iOS/Android تُنتج أرقامًا هندية افتراضيًا عند الكتابة، فبحث
 *  المستخدم عن "سورة ٢" أو "الدرس ٣" كان يفشل صمتًا أمام بيانات مخزَّنة
 *  بأرقام لاتينية (لا تطابق نصي مباشر، ولا Number("٢") يُحوَّل تلقائيًا في
 *  JS — يعيد NaN). راجع toArabicDigits في src/lib/utils.ts للاتجاه المعاكس
 *  (المستخدَم في عرض أرقام الآيات/الصفحات القرآنية فقط). */
const EASTERN_ARABIC_DIGITS: Record<string, string> = {
  "٠": "0", "١": "1", "٢": "2", "٣": "3", "٤": "4",
  "٥": "5", "٦": "6", "٧": "7", "٨": "8", "٩": "9",
  "۰": "0", "۱": "1", "۲": "2", "۳": "3", "۴": "4",
  "۵": "5", "۶": "6", "۷": "7", "۸": "8", "۹": "9",
};

/** يحوّل أي أرقام هندية عربية أو فارسية داخل نص إلى أرقام لاتينية عادية،
 *  مع ترك بقية النص كما هو تمامًا. آمن للاستدعاء على نص لا يحوي أرقامًا
 *  إطلاقًا (يعيده دون تغيير). */
export function toWesternDigits(text: string): string {
  if (!text) return text ?? "";
  return text.replace(/[٠-٩۰-۹]/g, (d) => EASTERN_ARABIC_DIGITS[d] ?? d);
}

/** يزيل التشكيل الكامل بما يشمل حركات مدّ القرآن وعلامات الوقف */
function removeTashkeel(text: string): string {
  return text
    // ─── الحركات الأساسية U+064B–U+065F ─────────────────────────
    .replace(/[ً-ٟ]/g, "")
    // ─── ألف خنجرية U+0670 ───────────────────────────────────────
    .replace(/ٰ/g, "")
    // ─── مدّات قرآنية U+0653–U+0655 ──────────────────────────────
    .replace(/[ٓ-ٕ]/g, "")
    // ─── علامات تجويد/ترتيل إضافية U+0616–U+061A ────────────────
    .replace(/[ؖ-ؚ]/g, "")
    // ─── علامات وقف قرآنية U+06D6–U+06DC و U+06DF–U+06E4 و U+06E7–U+06ED ──
    .replace(/[ۖ-ۜ۟-ۤۧ-ۭ]/g, "")
    // ─── الحروف العالية U+0610–U+061A (تشمل صلى الله عليه وسلم) ─
    .replace(/[ؐ-ؚ]/g, "")
    // ─── ممدودات قرآنية U+06E5–U+06E6 ───────────────────────────
    .replace(/[ۥ-ۦ]/g, "");
}

/**
 * يطبّع نصاً عربياً للفهرسة والبحث المقارن.
 *
 * خطوات التطبيع (بالترتيب):
 * 1. توحيد الأرقام الهندية/الفارسية إلى أرقام لاتينية (٢ ↔ 2)
 * 2. إزالة التشكيل الكامل (حركات + علامات وقف + مدّات قرآنية)
 * 3. توحيد أشكال الألف: أ إ آ ٱ → ا
 * 4. ؤ → و
 * 5. ئ → ي
 * 6. ة → ه
 * 7. ى → ي
 * 8. إزالة الكشيدة ـ
 * 9. توحيد المسافات
 * 10. إزالة علامات الترقيم الفاصلة
 *
 * @param text النص المراد تطبيعه
 * @returns النص المطبّع — لا يُعرض للمستخدم، للفهرسة فقط
 */
export function normalizeArabic(text: string): string {
  if (!text) return "";

  // ─── توحيد الأرقام (لوحة مفاتيح عربية على iOS/Android تكتب ٢ لا 2) ──
  let s = toWesternDigits(text);

  s = removeTashkeel(s);

  // ─── توحيد أشكال الألف ────────────────────────────────────────
  s = s.replace(/[أإآٱ]/g, "ا");

  // ─── ؤ → و ───────────────────────────────────────────────────
  s = s.replace(/ؤ/g, "و");

  // ─── ئ → ي ───────────────────────────────────────────────────
  s = s.replace(/ئ/g, "ي");

  // ─── ة → ه ───────────────────────────────────────────────────
  s = s.replace(/ة/g, "ه");

  // ─── ى → ي ───────────────────────────────────────────────────
  s = s.replace(/ى/g, "ي");

  // ─── إزالة الكشيدة ───────────────────────────────────────────
  s = s.replace(/ـ/g, "");

  // ─── إزالة علامات الترقيم الفاصلة ───────────────────────────
  // نحتفظ بالمسافات ونزيل: , . ; : ! ? ، ؛ ؟ « » " " ' ' ( ) [ ] { } - —
  s = s.replace(/[,.;:!?،؛؟«»""''()[\]{}\-—]/g, " ");

  // ─── توحيد المسافات ──────────────────────────────────────────
  s = s.replace(/\s+/g, " ").trim();

  return s;
}

/**
 * يتحقق من وجود needle في haystack بعد تطبيع الطرفين.
 * مناسب للبحث الجانب-العميل.
 */
export function normalizedIncludes(
  haystack: string | null | undefined,
  needle: string,
): boolean {
  if (!needle.trim()) return true;
  if (!haystack) return false;
  return normalizeArabic(haystack).includes(normalizeArabic(needle));
}

/**
 * يتحقق من وجود needle في أي من الحقول بعد التطبيع.
 */
export function normalizedMatchAny(
  fields: Array<string | null | undefined>,
  needle: string,
): boolean {
  return fields.some((f) => normalizedIncludes(f ?? "", needle));
}
