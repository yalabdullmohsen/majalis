export function filterMiraclesSeed(opts?: { category?: string; sourceType?: string }) {
  let rows = MIRACLES_SEED.filter((m) => m.status === "approved" && m.verification_status === "verified");
  if (opts?.category && opts.category !== "الكل") {
    rows = rows.filter((m) => m.category === opts.category);
  }
  if (opts?.sourceType && opts.sourceType !== "الكل") {
    rows = rows.filter((m) => m.source_type === opts.sourceType);
  }
  return rows;
}

export function searchMiraclesSeed(query: string) {
  const q = query.trim();
  if (!q) return [];
  return MIRACLES_SEED.filter((m) =>
    arabicMatchAny(
      [m.title, m.body, m.reference, m.category, m.scholarly_source, m.verse],
      q,
    ),
  );
}
