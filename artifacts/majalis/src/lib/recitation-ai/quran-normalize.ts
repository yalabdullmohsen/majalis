/**
 * quran-normalize.ts
 * تطبيع قرآني متخصص لطبقة مقارنة "اختبار التسميع بالذكاء الاصطناعي" فقط.
 *
 * ⚠️ قاعدة حرجة: هذا الملف لا يُعدّل نص القرآن المخزَّن أو المعروض إطلاقًا.
 *    النص المعروض للمستخدم يبقى دائمًا بالرسم العثماني الكامل من
 *    src/lib/quran-api.ts. التطبيع هنا يُنتج نسخة مؤقتة في الذاكرة فقط،
 *    تُستخدَم حصرًا لمقارنة الكلمة المنطوقة بالكلمة المكتوبة.
 *
 * يبني فوق src/shared/arabic-normalize.ts (نفس الوحدة المستخدَمة في محرك
 * البحث الموحّد) دون تعديلها، ويضيف طبقة قرآنية إضافية:
 *   1. جدول مرادفات الرسم العثماني ← النطق الحديث (الصلوة/الصلاة...).
 *   2. تجاهل البسملة في مقارنة بداية السور (منطق منفصل، انظر isBismillahWord).
 *
 * ما لا يُعالَج هنا عمدًا (حدود صادقة، موثَّقة في poc-results.md):
 *   - الإدغام/الإخفاء/الإقلاب الصوتي (مثال: "من ربهم" تُنطق "مِرَّبِّهم")
 *     يحتاج نموذج تحليل صوتي فونيمي حقيقي (نفس قدرة مستوى "إتقان
 *     التجويد" في القسم 3) — لا حل نصي بسيط موثوق متاح بلا مجازفة
 *     بإنذارات خاطئة. يبقى هذا فجوة معروفة في مستوى "حفظ فقط" أيضًا حتى
 *     يتوفر مزود ASR حقيقي قادر على تحليل السياق الصوتي المتصل.
 */
import { normalizeArabic } from "@/shared/arabic-normalize";

/**
 * جدول مرادفات الرسم العثماني ← النطق الحديث.
 * كل مفتاح هو الصورة المطبَّعة (بعد normalizeArabic) للرسم العثماني،
 * وقيمته هي الصورة المطبَّعة المكافئة بالإملاء الحديث. القائمة محدودة
 * عمدًا لكلمات موثّقة بثقة عالية فقط — لا استنتاج آلي عام لتفادي
 * تعارض المرادفات مع كلمات أخرى غير مرتبطة.
 *
 * كل زوج مبني على القراءة المعتمدة في المنصة (حفص عن عاصم) وعلى فحص
 * مباشر لنص المصحف المحلي الفعلي (public/data/quran/*.json)، لا افتراضًا
 * نظريًا.
 *
 * ⚠️ لم يُدرَج هنا عمدًا "ملك/مالك" رغم أن الفاتحة:4 مكتوبة فعليًا
 * "مَٰلِكِ" (بألف خنجرية، نفس ظاهرة إبراهيم/إسحاق) — لأن التحقّق المباشر
 * من البيانات (سورة الناس:2 "مَلِكِ ٱلنَّاسِ" بلا أي ألف خنجرية) أثبت أن
 * "ملك" بعد إزالة التشكيل تُصبح **نفس السلسلة النصية** سواء جاءت من
 * "مَلِكِ" (بلا ألف خنجرية، كلمة "الملك" الحقيقية) أو "مَٰلِكِ" (بألف
 * خنجرية، كلمة "المالك"). مرادفة عامة هنا كانت ستُخفي خطأ حفظ حقيقي
 * وشائع فعلاً (خلط "ملك"/"مالك" بين الفاتحة والناس، سورتان قصيرتان
 * تُحفظان معًا غالبًا). الإصلاح الصحيح لكلمة الفاتحة:4 تحديدًا يقع في
 * طبقة بناء الكلمات المرجعية (quran-reference-words.ts) كتصحيح خاص
 * بموضعها الدقيق فقط، لا كقاعدة عامة على كل ظهور لكلمة "ملك" في المصحف.
 */
const UTHMANI_SPELLING_SYNONYMS: Record<string, string> = buildSynonymMap([
  ["الصلوه", "الصلاه"],       // الصلوة ← الصلاة (رسم بالواو بدل الألف)
  ["الزكوه", "الزكاه"],       // الزكوة ← الزكاة
  ["الحيوه", "الحياه"],       // الحيوة ← الحياة
  ["مشكوه", "مشكاه"],         // مشكوة ← مشكاة (النور: 35)
  ["ابرهيم", "ابراهيم"],      // إبرٰهيم (ألف خنجرية فوق الراء تُحذف بالتطبيع
                               // العام فتضيع حرفًا كاملاً) ← إبراهيم بألف صريحة
  ["اسحق", "اسحاق"],          // إسحٰق (نفس ظاهرة الألف الخنجرية) ← إسحاق
  ["اسمعيل", "اسماعيل"],      // إسمٰعيل (نفس الظاهرة) ← إسماعيل
  ["العلمين", "العالمين"],    // ٱلْعَٰلَمِينَ (نفس ظاهرة الألف الخنجرية، هذه
                               // المرة وسط الكلمة لا في أولها) ← العالمين
                               // بألف صريحة. آمنة عامةً (خلافًا لملك/مالك):
                               // "علمين" ليست كلمة عربية حقيقية منفصلة قد
                               // تتعارض معها هذه المرادفة. اكتُشفت فعليًا
                               // أثناء بناء فهرس "التسميع الحر" (راجع
                               // freeform-start-detector.test.ts) — بلا هذا
                               // الإصلاح، أي مستخدم ينطق "العالمين" (رب
                               // العالمين، شائعة جدًا) وتُنسخها ASR بالرسم
                               // الحديث كان سيُصنَّف خطأً وهميًا.
  // ⚠️ حدّ معروف غير مُعالَج: نفس الظاهرة (ألف خنجرية وسط كلمة على وزن
  // "فَاعِل") تتكرر في كلمات أخرى غير مُدقَّقة هنا فرديًا بعد (مثال:
  // "ظالمين"/"الظالمين" ← "ٱلظَّٰلِمِينَ")، لم تُضَف عمدًا بلا تحقّق مباشر
  // من كل موضع كما جرى مع العالمين أعلاه — تحسين مستقبلي محتمل.
]);
// ملاحظة تحقُّق: هذا/ذلك/لكن/هؤلاء/الرحمن تحوي ألفًا خنجرية أيضًا في
// الرسم العثماني، لكن التطبيع العام (حذف التشكيل) يُنتج **نفس** الصورة
// المطابقة للإملاء الحديث القياسي لهذه الكلمات تحديدًا (كلاهما بلا ألف
// صريحة أصلاً في العربية الفصحى المعاصرة) — فلا حاجة لإدراجها هنا، خلافًا
// لإبراهيم/إسحاق/إسماعيل حيث الإملاء الحديث يستخدم ألفًا صريحة زائدة.

function buildSynonymMap(pairs: Array<[string, string]>): Record<string, string> {
  const map: Record<string, string> = {};
  for (const [uthmani, modern] of pairs) {
    if (uthmani === modern) continue;
    // كلا الاتجاهين يُشيران لنفس "الصورة الكنسية" (شكل الرسم العثماني)
    // حتى يتطابق أيًّا كان الشكل الذي أنتجه ASR.
    map[uthmani] = uthmani;
    map[modern] = uthmani;
  }
  return map;
}

/**
 * يطبّع كلمة قرآنية واحدة (أو نصًا قصيرًا) لغرض المقارنة الصوتية فقط.
 * يُطبَّق على كل من النص المرجعي المكتوب والنص المنطوق المتعرَّف عليه
 * بنفس الطريقة تمامًا، لضمان الإنصاف في المقارنة.
 */
export function normalizeQuranWord(word: string): string {
  // BOM/ZWNBSP (U+FEFF) وُجد فعليًا بادئًا لبعض ملفات public/data/quran/*.json
  // (تحقَّق مباشرة: أول حرف في سورة-001.json تحديدًا) — يُزال دفاعيًا هنا
  // مهما كان مصدر النص، لأن normalizeArabic (المشتركة) لا تتوقّعه.
  const clean = word.replace(/^\uFEFF/, "");
  const base = normalizeArabic(clean);
  return UTHMANI_SPELLING_SYNONYMS[base] ?? base;
}

/**
 * تصحيحات مطبَّعة خاصة بموضع دقيق (سورة:آية:فهرس الكلمة) لا بكل ظهور
 * للكلمة في المصحف — للحالات التي يفشل فيها التطبيع العام تحديدًا في
 * ذلك الموضع فقط دون أن يصح تعميمها (انظر تعليق "ملك/مالك" أعلاه).
 * تُستهلَك من quran-reference-words.ts عند بناء كل ReferenceWord.
 */
export const WORD_POSITION_OVERRIDES: Record<string, string> = {
  // الفاتحة:4:0 — "مَٰلِكِ" (ألف خنجرية) ← "مالك" فقط في هذا الموضع تحديدًا
  "1:4:0": "مالك",
};

export function positionKey(surah: number, ayah: number, wordIndex: number): string {
  return `${surah}:${ayah}:${wordIndex}`;
}

/** يطبّع مقطعًا كاملاً (آية أو أكثر) كلمة كلمة، محافظًا على ترتيب الكلمات. */
export function normalizeQuranText(text: string): string[] {
  return text
    .split(/\s+/)
    .filter(Boolean)
    .map(normalizeQuranWord);
}

/**
 * البسملة: "بِسْمِ ٱللَّهِ ٱلرَّحْمَٰنِ ٱلرَّحِيمِ" — لا تُحتسب زيادةً أو
 * نقصًا في مقارنة بداية أي سورة عدا الفاتحة (حيث هي الآية 1 فعليًا في
 * بيانات المصحف المحلية) والتوبة (لا بسملة لها إطلاقًا في المصحف).
 * تُستخدَم في VerseAlignmentEngine لتجاوز البسملة إن نطقها المستخدم أو
 * تركها دون تنبيه في السور الأخرى.
 */
const BISMILLAH_NORMALIZED = normalizeQuranText("بسم الله الرحمن الرحيم").join(" ");

export function isBismillahPhrase(normalizedWords: string[]): boolean {
  if (normalizedWords.length < 3) return false;
  const candidate = normalizedWords.slice(0, 4).join(" ");
  return (
    candidate === BISMILLAH_NORMALIZED ||
    BISMILLAH_NORMALIZED.startsWith(candidate) ||
    candidate.startsWith(BISMILLAH_NORMALIZED)
  );
}

