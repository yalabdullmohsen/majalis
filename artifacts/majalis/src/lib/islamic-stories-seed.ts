export function filterIslamicStoriesSeed(opts?: {
  category?: string;
  era?: string;
  search?: string;
}): IslamicStorySeed[] {
  let items = ISLAMIC_STORIES_SEED;
  if (opts?.category && opts.category !== "الكل") {
    items = items.filter((s) => s.category === opts.category);
  }
  if (opts?.era && opts.era !== "الكل") {
    items = items.filter((s) => s.era === opts.era);
  }
  if (opts?.search?.trim()) {
    items = items.filter((s) =>
      arabicMatchAny([s.title, s.summary, ...s.tags], opts.search!),
    );
  }
  return items;
}
