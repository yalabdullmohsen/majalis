export function filterIslamicStoriesSeed(opts?: {
  category?: string;
  era?: string;
  search?: string;
}): IslamicStorySeed[] {
  let items = ISLAMIC_STORIES_SEED;
  if (opts?.category && opts.category !== "الكل") {
    items = items.filter((s) => s.category === opts.category);
  }
  if (opts?.era && opts.era !== "الكل") {
    items = items.filter((s) => s.era === opts.era);
  }
  if (opts?.search?.trim()) {
    const q = opts.search.toLowerCase();
    items = items.filter(
      (s) =>
        s.title.toLowerCase().includes(q) ||
        s.summary.toLowerCase().includes(q) ||
        s.tags.some((t) => t.toLowerCase().includes(q)),
    );
  }
  return items;
}
