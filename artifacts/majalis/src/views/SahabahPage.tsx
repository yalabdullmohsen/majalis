/* ─── صحابي اليوم ─── */
function todaysSahabi(): Sahabi {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((now.getTime() - start.getTime()) / 86400000);
  return SAHABAH[(dayOfYear - 1 + SAHABAH.length) % SAHABAH.length];
}

