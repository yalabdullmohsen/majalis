/* ─── خُلُق اليوم ─── */
function todaysAkhlaq(): AkhlaqEntry {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((now.getTime() - start.getTime()) / 86400000);
  return AKHLAQ[(dayOfYear - 1 + AKHLAQ.length) % AKHLAQ.length];
}

